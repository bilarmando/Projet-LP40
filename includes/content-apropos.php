                <h2>&agrave; propos de la Ligue 1</h2>
                <p>Le championnat de France de football masculin, dénommé Ligue 1 (L1), est une compétition de football qui est pour la France le premier échelon national. Il se déroule annuellement sous forme d'un championnat opposant vingt clubs professionnels. Une saison du championnat commence en été et se termine au printemps suivant. La première journée de l'édition inaugurale se tient le 11 septembre 1932. Nommé « Division Nationale » lors des premières éditions, le championnat prend ensuite le nom de « Division 1 » (D1) en 1972, avant de prendre son nom actuel à l'été 2002.
                    L'AS Saint-Étienne est le club le plus couronné avec dix titres de champion de France alors que l'Olympique lyonnais est le club qui a remporté le plus de titres consécutifs (sept entre 2002 et 2008). Le Paris Saint-Germain est quadruple tenant du titre après sa victoire lors de l'édition 2015-2016.
                    Avec 66 saisons de présence en Ligue 1, le Football Club Sochaux-Montbéliard et l'Olympique de Marseille détiennent le record du nombre de saisons parmi l'élite, alors que le Football Club de Nantes détient quant à lui le record de longévité en Ligue 1 avec 44 saisons consécutives (de 1963 à 2007). Le Paris Saint-Germain est actuellement le doyen de la Ligue 1 puisqu'il y est présent depuis 1974 (soit sa 42e année consécutive en 2015-2016).</p>
